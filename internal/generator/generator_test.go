// === Provider Resources Validation ===

func TestProviderResourcesComplete(t *testing.T) {
	// Verify all 4 OCI resources are registered
	expectedResources := []string{
		"compartment",
		"vcn",
		"subnet",
		"bucket",
		// REMOVIDOS: instance, database, cluster
	}

	ociResources, ok := ProviderResources["oci"]
	if !ok {
		t.Fatal("OCI provider not registered in ProviderResources")
	}

	for _, r := range expectedResources {
		if _, exists := ociResources[r]; !exists {
			t.Errorf("Missing OCI resource '%s' in ProviderResources", r)
		}
	}

	if len(ociResources) != 4 {
		t.Errorf("Expected 4 OCI resources, got %d", len(ociResources), len(ociResources))
	}
}

