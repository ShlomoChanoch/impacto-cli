// === Idempotência ===

func TestPrintTemplateIdempotency(t *testing.T) {
	first, _, err1 := captureOutput(func() error {
		return PrintTemplate("oci", "bucket")
	})

	second, _, err2 := captureOutput(func() error {
		return PrintTemplate("oci", "bucket")
	})

	if err1 != nil || err2 != nil {
		t.Fatalf("Expected no errors for idempotency test, got: err1=%v, err2=%v", err1, err2)
	}

	if first != second {
		t.Error("Template output is not idempotent — two identical calls produced different results")
	}
}

