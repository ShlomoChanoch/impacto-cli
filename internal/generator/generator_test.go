func captureOutput(f func() error) (string, string, error) {
	oldStdout := os.Stdout
	oldStderr := os.Stderr

	rOut, wOut, _ := os.Pipe()
	rErr, wErr, _ := os.Pipe()

	os.Stdout = wOut
	os.Stderr = wErr

	err := f()

	wOut.Close()
	wErr.Close()

	os.Stdout = oldStdout
	os.Stderr = oldStderr

	var bufOut, bufErr bytes.Buffer
	bufOut.ReadFrom(rOut)
	bufErr.ReadFrom(rErr)

	return bufOut.String(), bufErr.String(), err
}

// === Testes de geração de templates ===

func TestPrintTemplateAllResources(t *testing.T) {
	tests := []struct {
		name     string
		provider string
		resource string
		required []string
	}{
		{
			name:     "Compartment",
			provider: "oci",
			resource: "compartment",
			required: []string{
				"# Generated by Impacto CLI",
				"kind: Compartment",
				"providerConfigRef:",
				"deletionPolicy:",
				"# CHANGE:",
			},
		},
		{
			name:     "VCN",
			provider: "oci",
			resource: "vcn",
			required: []string{
				"# Generated by Impacto CLI",
				"kind: Vcn",
				"providerConfigRef:",
				"cidrBlock",
				"compartmentId",
				"# CHANGE:",
			},
		},
		{
			name:     "Subnet",
			provider: "oci",
			resource: "subnet",
			required: []string{
				"# Generated by Impacto CLI",
				"kind: Subnet",
				"providerConfigRef:",
				"vcnId",
				"cidrBlock",
				"# CHANGE:",
			},
		},
		{
			name:     "Bucket",
			provider: "oci",
			resource: "bucket",
			required: []string{
				"# Generated by Impacto CLI",
				"kind: Bucket",
				"providerConfigRef:",
				"storageTier",
				"# CHANGE:",
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			stdout, stderr, err := captureOutput(func() error {
				return PrintTemplate(tt.provider, tt.resource)
			})

			if err != nil {
				t.Fatalf("Expected no error for '%s %s', got: %v", tt.provider, tt.resource, err)
			}

			if stderr != "" {
				t.Errorf("Expected empty stderr, got: %s", stderr)
			}

			for _, expected := range tt.required {
				if !strings.Contains(stdout, expected) {
					t.Errorf("%s template missing '%s'", tt.name, expected)
				}
			}
		})
	}
}

// === Idempotência ===

func TestPrintTemplateIdempotency(t *testing.T) {
	tests := []struct {
		provider string
		resource string
	}{
		{"oci", "bucket"},
		{"oci", "compartment"},
		{"oci", "vcn"},
		{"oci", "subnet"},
	}

	for _, tt := range tests {
		t.Run(tt.provider+"/"+tt.resource, func(t *testing.T) {
			first, _, err1 := captureOutput(func() error {
				return PrintTemplate(tt.provider, tt.resource)
			})

			second, _, err2 := captureOutput(func() error {
				return PrintTemplate(tt.provider, tt.resource)
			})

			if err1 != nil || err2 != nil {
				t.Fatalf("Expected no errors for idempotency test, got: err1=%v, err2=%v", err1, err2)
			}

			if first != second {
				t.Error("Template output is not idempotent — two identical calls produced different results")
			}
		})
	}
}

