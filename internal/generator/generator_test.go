func TestPrintTemplateInvalidProvider(t *testing.T) {
	stdout, _, err := captureOutput(func() error {
		return PrintTemplate("aws", "bucket")
	})

	if err == nil {
		t.Fatal("Expected error for unknown provider 'aws', got nil")
	}

	if !strings.Contains(err.Error(), "unknown provider") {
		t.Errorf("Error should indicate unknown provider, got: %v", err)
	}

	if !strings.Contains(err.Error(), "Available providers") {
		t.Errorf("Error should list available providers, got: %v", err)
	}

	if stdout != "" {
		t.Errorf("Expected empty stdout on error, got: %s", stdout)
	}
}

