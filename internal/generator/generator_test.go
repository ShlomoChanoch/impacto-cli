func captureOutput(f func() error) (string, string, error) {
	oldStdout := os.Stdout
	oldStderr := os.Stderr

	rOut, wOut, _ := os.Pipe()
	rErr, wErr, _ := os.Pipe()

	os.Stdout = wOut
	os.Stderr = wErr

	err := f()

	wOut.Close()
	wErr.Close()

	os.Stdout = oldStdout
	os.Stderr = oldStderr

	var bufOut, bufErr bytes.Buffer
	bufOut.ReadFrom(rOut)
	bufErr.ReadFrom(rErr)

	return bufOut.String(), bufErr.String(), err
}

// === OCI Compartment ===

func TestPrintTemplateCompartment(t *testing.T) {
	stdout, stderr, err := captureOutput(func() error {
		return PrintTemplate("oci", "compartment")
	})

	if err != nil {
		t.Fatalf("Expected no error for 'oci compartment', got: %v", err)
	}

	if stderr != "" {
		t.Errorf("Expected empty stderr, got: %s", stderr)
	}

	required := map[string]string{
		"apiVersion":        "apiVersion: identity.oci.upbound.io/v1alpha1",
		"kind":              "kind: Compartment",
		"generated header":  "# Generated by Impacto CLI",
		"providerConfigRef": "providerConfigRef:",
		"deletionPolicy":    "deletionPolicy: Delete",
	}

	for label, expected := range required {
		if !strings.Contains(stdout, expected) {
			t.Errorf("Compartment template missing %s: expected '%s' not found", label, expected)
		}
	}

	if !strings.Contains(stdout, "name: default") {
		t.Error("Compartment template should reference providerConfigRef.name: default")
	}

	if !strings.Contains(stdout, "# CHANGE:") {
		t.Error("Compartment template should contain CHANGE comments for user customization")
	}
}

