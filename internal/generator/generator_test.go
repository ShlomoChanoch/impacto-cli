func TestResourceDescription(t *testing.T) {
	tests := []struct {
		provider string
		resource string
		want     string
	}{
		{"oci", "compartment", "OCI Compartment"},
		{"oci", "vcn", "Virtual Cloud Network"},
		{"oci", "subnet", "Subnet dentro"},
		{"oci", "bucket", "Object Storage Bucket"},
		{"oci", "nonexistent", ""},
		{"aws", "bucket", ""},
	}

	for _, tt := range tests {
		t.Run(tt.provider+"/"+tt.resource, func(t *testing.T) {
			got := ResourceDescription(tt.provider, tt.resource)
			if tt.want == "" && got != "" {
				t.Errorf("Expected empty description, got '%s'", got)
			}
			if tt.want != "" && !strings.Contains(got, tt.want) {
				t.Errorf("Expected description containing '%s', got '%s'", tt.want, got)
			}
		})
	}
}
